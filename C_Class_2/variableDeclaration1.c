#include <stdio.h>

int main()
{
    int num1 = 10;
    float num2 = 3.75;
    double num3 = 3.755;
    char grade = 'A';

    printf("The value of num1 variable is: %d\n",num1);
    printf("The value of num2 variable is %f\n",num2);
    printf("The value of num3 variable is %lf\n",num3);
    printf("The value of grade variable is %c",grade);

    return 0;
}
